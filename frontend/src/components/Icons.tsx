/**
 * Icons — small inline SVGs (no icon library; the dark studio ships zero extra
 * deps). Each takes an optional `size`; stroke uses currentColor so they inherit
 * the surrounding ink/accent color. 24px viewBox, 2px stroke, round caps.
 */
interface IconProps {
  size?: number;
}

function svg(size: number, children: React.ReactNode) {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={2}
      strokeLinecap="round"
      strokeLinejoin="round"
      aria-hidden="true"
    >
      {children}
    </svg>
  );
}

